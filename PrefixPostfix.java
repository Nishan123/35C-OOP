public class PrefixPostfix {
    public static void main(String[] args) {
        // Prefix operation 
        int PrefixValue = 10;
        System.out.println("Prefix Value in first line : "+ ++PrefixValue);
        System.out.println("Prefix Value in second line : "+ PrefixValue);


        // Postfix operation
        int PostifxValue = 10;
        System.out.println("Postfix value in first line  "+ PostifxValue++);
        System.out.println("Postfix value in second line  "+ PostifxValue);

    }
}
